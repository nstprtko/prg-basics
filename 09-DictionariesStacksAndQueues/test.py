# Create a dictionary
fruits = {'apple': "green", 'banana': "yellow", 'orange': "orange"}

# Iterating over keys
for fruit in fruits:
   print(fruit)

# Iterating over values
for count in fruits.values():
   print(count)

# Iterating over key-value pairs
for fruit, count in fruits.items():
   print(f"The count of {fruit} is {count}")